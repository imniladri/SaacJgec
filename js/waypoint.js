// Waypoint Function Init

// Default
//(Start Animation at 50% from Top)
$(".waypoint").waypoint(
    function (direction) {
        if (direction == "down") {
            $(".waypoint").addClass("viewpoint");
        } else {
            $(".waypoint").removeClass("viewpoint");
        }
    },
    {
        offset: "50%",
    }
);
//=========================================================================

